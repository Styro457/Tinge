class Rectangle {

    constructor(x, y, width, height) {
        /**
         * The x coordinate of the rectangles bottom left corner
         * @name x
         * @type {number}
         */
        this.x = x;

        /**
         * The y coordinate of the rectangles bottom left corner
         * @name y
         * @type {number}
         */
        this.y = y;

        /**
         * The width of the rectangle
         * @name width
         * @type {number}
         */
        this.width = width;

        /**
         * The height of the rectangle
         * @name height
         * @type {number}
         */
        this.height = height;
    }

}