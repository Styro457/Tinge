class Circle {

    constructor(x, y, radius) {
        /**
         * The x coordinate of the circles center
         * @type {number}
         * @public
         */
        this.x = x;

        /**
         * The y coordinate of the circles center
         * @type {number}
         * @public
         */
        this.y = y;

        /**
         * The radius of the circle
         * @type {number}
         * @public
         */
        this.radius = radius;
    }

}

export default Circle;